    private void btnRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroActionPerformed
        String numerolibro = txtNumeroLibro.getText();
        int numlibro = Integer.parseInt(numerolibro);
        
        biblioteca[numlibro] = txtNombreBiblioteca.getText();
        ubicacion[numlibro] = txtUbicacion.getText();
        estatus[numlibro] = txtEstatus.getText();
        
        titulo[numlibro] = txtTitulo.getText();
        autor[numlibro] = txtAutor.getText();
        editorial[numlibro] = txtEditorial.getText();
        codigo[numlibro] = txtCodigo.getText();
    }//GEN-LAST:event_btnRegistroActionPerformed

