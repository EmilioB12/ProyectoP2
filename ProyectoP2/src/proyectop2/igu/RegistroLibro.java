    private void btnAtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtrasActionPerformed
        String numerolibro = txtNumeroLibro.getText();
        int numlibro = Integer.parseInt(numerolibro);    
        if (numlibro>0){
            numlibro = numlibro+1;
            numerolibro = String.valueOf(numlibro);
            txtNumeroLibro.setText(numerolibro);
            
            txtNombreBiblioteca.setText(biblioteca[numlibro]);
            txtUbicacion.setText(ubicacion[numlibro]);
            txtEstatus.setText(estatus[numlibro]);
            
            txtTitulo.setText(titulo[numlibro]);
            txtAutor.setText(autor[numlibro]);
            txtEditorial.setText(editorial[numlibro]);
            txtCodigo.setText(codigo[numlibro]);
        }
    }//GEN-LAST:event_btnAtrasActionPerformed

